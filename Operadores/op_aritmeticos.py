#Suma y resta (+,-)
suma = 12 + 5
print(suma)

resta = 12 - 5
print(resta)

#Multiplicacion y division (*,/)
multi = 12 * 5
print(multi)

division = 12/5 #Devuelve un dato flotante
print(division)

#Potencia (exponente) (**)
exponente = 12 ** 5
print(exponente)

#Division baja (//) devuelve entero redondeado hacia abajo. int
division_baja = 12 // 5
print(division_baja)

#Residuo o modulo muestra el residuo
residuo = 12 % 5
print(residuo)

#Para saber el tipo de dato de un resultado o variable se coloca
tipo_de_dato = type(5)
print(tipo_de_dato)

